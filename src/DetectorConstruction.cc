void DetectorConstruction::DefineMaterials()
{
  G4bool isotopes = false;
  G4int ncomponents; 
  G4double fractionmass;

  G4Element* C = new G4Element("Carbon", "C", 6, 12.011*g/mole);
  G4Element* Cr = new G4Element("Chromium","Cr", 24., 52.00*g/mole);
  G4Element* Si = new G4Element("Silicon", "Si", 14., 28.09*g/mole);
  G4Element* Mn = new G4Element("Manganese", "Mn", 25., 54.94*g/mole);
  G4Element* Fe = new G4Element("Ferrum","Fe", 26., 55.85*g/mole);
  G4Element* Ni = new G4Element("Nickel","Ni", 28., 58.70*g/mole);
  G4Element* Ti = new G4Element("Titanium", "Ti", 22., 47.88*g/mole);

  // build materials
  //
  G4Material* steel_mat = 
  new G4Material("steel_mat", 7.9 * g / cm3, ncomponents=7, kStateSolid, 293.*kelvin, 1.*atmosphere);
  
    steel_mat->AddElement(C,  fractionmass=0.0012);
    steel_mat->AddElement(Ti, fractionmass=0.0046);
    steel_mat->AddElement(Cr, fractionmass=0.1811);
    steel_mat->AddElement(Ni, fractionmass=0.1215);
    steel_mat->AddElement(Si, fractionmass=0.0046);
    steel_mat->AddElement(Mn, fractionmass=0.0107);
    steel_mat->AddElement(Fe, fractionmass=0.6763);

  G4Element* N  = new G4Element("Nitrogen", "N", 7, 14.01*g/mole);
  G4Element* O  = new G4Element("Oxygen",   "O", 8, 16.00*g/mole);
  //
    
  G4Material* Air20 = new G4Material("Air", 1.205*mg/cm3, ncomponents=2,
                      kStateGas, 293.*kelvin, 1.*atmosphere);
    Air20->AddElement(N, fractionmass=0.7);
    Air20->AddElement(O, fractionmass=0.3);
  //
  fWorldMater = Air20;
  fDetectorMater = steel_mat;
  
  // or use G4 materials data base
  //
  G4NistManager* man = G4NistManager::Instance();  
  fTargetMater = man->FindOrBuildMaterial("G4_Ca");
                   
 ///G4cout << *(G4Material::GetMaterialTable()) << G4endl;
}

